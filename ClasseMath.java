public class ClasseMath {
    
    /*
     * La classe Math permette di fare delle operazioni matematica utili
     */

    public void usoMath() {                            //uso un metodo altrimenti non mi fa compilare la classe, poi li spiego

        System.out.println(Math.abs(-55));              //da valori assoluti
        System.out.println(Math.min(55, 21));       //da il minimo tra due valori
        System.out.println(Math.max(55, 21));       //da il massimo tra due valori
        System.out.println(Math.pow(5, 3));         //elevazione a potenza, il primo numero è la base e il secondo è l'esponente
        System.out.println(Math.sqrt(25));            //da la radice quadrata
        System.out.println(Math.PI);                    //da il pi greco                 

    }

}
